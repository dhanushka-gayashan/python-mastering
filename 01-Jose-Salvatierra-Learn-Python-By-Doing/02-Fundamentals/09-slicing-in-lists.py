print(friends[:4])


# With Negative -> 4(-3), 5(-2), 6(-1)
print(friends[-3:])


# With Negative -> 0(-7), 1(-6), 2(-5), 3(-4), 4(-3)
print(friends[:-2])


# With Negative ->  3(-4), 4(-3)
print(friends[-4:5])


# With Negative -> 2(-5), 3(-4)
print(friends[2:-3])


