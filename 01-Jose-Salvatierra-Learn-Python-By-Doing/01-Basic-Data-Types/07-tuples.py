# Define
short_tuple = "Dhanushka", "Gayashan"
print(short_tuple)

clear_tuple = ("Dhanushka", "Gayashan")
print(clear_tuple)


# Single Item Tuple
short_tuple = "Dhanushka",
print(short_tuple)

clear_tuple = ("Dhanushka", )
print(clear_tuple)


# List of Tuples
friends = [("Dhanushka",  "Gayashan"), ("Chamari", "Nimalka")]
print(friends)


# Append - Cannot Add or Remove
friends = ("Dhanushka", "Gayashan") + ("Chamari",)
print(friends)
